class NationalTeamCareer:
    def __init__(self, player_id, national_team:str, debut:str, matches:int, goals:int, coach:str, age_at_debut:int):
        self.player_id = player_id
        self.national_team = national_team
        self.debut = debut
        self.matches = matches
        self.goals = goals
        self.coach = coach
        self.age_at_debut = age_at_debut